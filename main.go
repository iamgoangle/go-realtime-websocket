func home(w http.ResponseWriter, r *http.Request) {
	homeTemplate.Execute(w, "ws://"+r.Host+"/ws")
}

func handleConnections(w http.ResponseWriter, r *http.Request) {
	// TODO: try error, the frontend should redirect
	// log.Println(r)
	// http.Error(w, "Test API Gateway error", http.StatusUnauthorized)
	// io.WriteString(w, "Hello, world!\n")
	// return

	// Upgrade initial GET request to a websocket
	ws, err := upgrader.Upgrade(w, r, nil)
	if err != nil {
		log.Fatal(err)
	}
	// Make sure we close the connection when the function returns
	defer ws.Close()

	// Register our new client
	clients[ws] = true

	// TODO: Error something, but socket still open
	// ws.WriteMessage(websocket.TextMessage, []byte(`{"status": "Unauthorized"}`))
	// return

	for {
		var msg Message
		// Read in a new message as JSON and map it to a Message object
		err := ws.ReadJSON(&msg)
		if err != nil {
			log.Printf("error aa: %v", err)
			delete(clients, ws)
			break
		}
		// Send the newly received message to the broadcast channel
		broadcast <- msg
	}
}

func handleMessages() {
	for {
		// Grab the next message from the broadcast channel
		msg := <-broadcast
		// Send it out to every client that is currently connected
		for client := range clients {
			log.Println(msg)

			// err := client.WriteJSON(msg)
			err := client.WriteMessage(websocket.TextMessage, []byte(msg.Message))
			if err != nil {
				log.Printf("error xx: %v", err)
				client.Close()
				delete(clients, client)
			}
		}
	}
}
